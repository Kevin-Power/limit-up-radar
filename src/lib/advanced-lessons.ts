export function getAdvancedLessonBySlug(slug: string): Lesson | undefined {
  return ADVANCED_LESSONS.find((l) => l.slug === slug);
}

export function getAdjacentAdvancedLessons(slug: string) {
  const idx = ADVANCED_LESSONS.findIndex((l) => l.slug === slug);
  return {
    prev: idx > 0 ? ADVANCED_LESSONS[idx - 1] : null,
    next: idx < ADVANCED_LESSONS.length - 1 ? ADVANCED_LESSONS[idx + 1] : null,
  };
}

// 由各課 readTime（如 "7 分鐘"）累加出總閱讀分鐘數，避免首頁文案與內容飄移。
export function totalReadMinutes(): number {
  return ADVANCED_LESSONS.reduce((sum, l) => {
    const m = l.readTime.match(/\d+/);
    return sum + (m ? Number(m[0]) : 0);
  }, 0);
}
